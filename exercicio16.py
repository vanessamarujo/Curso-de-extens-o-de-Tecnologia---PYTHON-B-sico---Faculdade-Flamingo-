# Exercício de Cálculo de IMC

print ("Programa para descobrir seu IMC (Índice de massa corporal)")
altura= float (input("Digite sua altura : "))
peso= float (input(" Agora digite o seu peso atual: "))
imc= peso/(altura*altura)
print (" Seu IMC é : ", imc)
