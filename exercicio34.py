# Exercício  Escreva um algoritmo para ler uma temperatura em graus Fahrenheit, calcular e escrever o valor correspondente 
# em graus Celsius (baseado na fórmula abaixo):


F = float(input("Digite a temperatura em graus Fahrenheit"))
C = (F - 32) * ( 5 / 9)
print(" O valor correspondente aos graus Celsius é",C)
